duckx::Paragraph &
duckx::Paragraph::append(const char *text, duckx::formatting_flag f, unsigned char nFontSize, const char *pszFontName, int nColor)
{
    if (current == NULL)
    {
        current = parent.append_child("w:p");
    }
    else
    {
        current = parent.append_copy(current);
    }
    add_run(text, f, nFontSize, pszFontName, nColor);
    return *this;
}

duckx::Paragraph &duckx::Paragraph::set_alignment(paragraph_alignment a, bool bNew, unsigned char indent, unsigned char font_size)
{
    if (bNew == true || current == NULL)
    {
        current = parent.append_child("w:p");
    }
    run.set_parent(current);

    pugi::xml_node meta = current.append_child("w:pPr");
    if (indent > 0)
    {
        if (font_size == 0)
        {
            font_size = 10;
        }
        pugi::xml_node node = meta.append_child("w:ind");
        node.append_attribute("w:firstLine").set_value(indent * font_size * 100);
        node.append_attribute("w:firstLineChars").set_value(indent * 100);
    }

    switch (a)
    {
    case duckx::align_left: break;
    case duckx::align_center: meta.append_child("w:jc").append_attribute("w:val").set_value("center"); break;
    case duckx::align_right: meta.append_child("w:jc").append_attribute("w:val").set_value("right"); break;
    case duckx::align_both: meta.append_child("w:jc").append_attribute("w:val").set_value("both"); break;
    default: break;
    }

    return *this;
}

bool duckx::Paragraph::set_text(const char *lpszT, duckx::formatting_flag f, int nColor)
{
    if (current == NULL)
    {
        current = parent.append_child("w:p");
        run.set_parent(current);
    }
    if (run.has_next())
    {
        run.set_text(lpszT);
    }
    else
    {
        add_run(lpszT, f, 0, NULL, nColor);
    }
    return true;
}

void duckx::Paragraph::add(pugi::xml_node node)
{
    parent = node;
    current = parent.child("w:p");
    if (current == NULL)
    {
        current = parent.append_child("w:p");
    }
    run.set_parent(current);
}

