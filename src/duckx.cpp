void duckx::TableRow::resize(int nRow, int nCol)
{
    this->current = this->parent.child("w:tr");
    if (current == NULL)
    {
        current = parent.append_child("w:tr");
        pugi::xml_node meta = current.append_child("w:tblPrEx").append_child("w:tblBorders");
        meta.append_child("w:top").append_attribute("w:val").set_value("single");
        meta.append_child("w:left").append_attribute("w:val").set_value("single");
        meta.append_child("w:bottom").append_attribute("w:val").set_value("single");
        meta.append_child("w:right").append_attribute("w:val").set_value("single");
        meta.append_child("w:insideH").append_attribute("w:val").set_value("single");
        meta.append_child("w:insideV").append_attribute("w:val").set_value("single");
    }
    cell.set_parent(this->current);
    cell.resize(nCol);

    pugi::xml_node cur;
    int nR = 0;
    while (current != NULL)
    {
        if (nR < nRow)
        {
            cells().resize(nCol);
        }
        nR++;
        cur = current;
        current = current.next_sibling();
    }

    int i;
    for (i = nR; i < nRow; i++)
    {
        pugi::xml_node added = parent.append_copy(cur);
        TableRow tr(parent, added);
    }
    for (i = nRow; i < nR; i++)
    {
        parent.remove_child(parent.last_child());
    }
}

void duckx::TableRow::merge(int nStartRow, int nStartCol, int nSpanRow, int nSpanCol)
{
    int i, j;
    for (i = 0; i < nStartRow; i++)
    {
        current = current.next_sibling();
        if (current == NULL)
        {
            return;
        }
    }
    
    if (current == NULL)
    {
        return;
    }

    if (nSpanRow <= 1)
    {
        if (nSpanCol > 1)
        {
            auto tc = cells();
            for (j = 0; j < nStartCol; j++)
            {
                tc.next();
            }
            tc.mergeCol(nSpanCol);
        }
    }
    else
    {
        for (i = 0; i < nSpanRow; i++)
        {
            auto tc = cells();
            for (j = 0; j < nStartCol; j++)
            {
                tc.next();
            }
            tc.mergeRow(i == 0);
            if (nSpanCol > 1)
            {
                tc.mergeCol(nSpanCol);
            }
            current = current.next_sibling();
        }
    }
}

