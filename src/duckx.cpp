void duckx::Table::resize(int nRow, int nCol)
{
    if (current == NULL)
    {
        current = parent.append_child("w:tbl");
    }
    row.set_parent(current);

    if (nRow > 0 && nCol > 0)
    {
        row.resize(nRow, nCol);
    }
}

void duckx::Table::merge(int nStartRow, int nStartCol, int nSpanRow, int nSpanCol)
{
    if (nSpanRow < 0 || nStartCol < 0 || nSpanRow < 0 || nSpanCol < 0)
    {
        return;
    }
    rows().merge(nStartRow, nStartCol, nSpanRow, nSpanCol);
}

